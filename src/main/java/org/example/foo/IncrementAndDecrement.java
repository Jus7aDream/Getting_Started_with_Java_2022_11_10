public class IncrementAndDecrement {
    public static void main(String[] args) {
        int number1 = 0;
//        number1 = number1 + 1;
        number1++;
        System.out.println(number1);

        int number2 = 0;
//        number2 = number2 -1;
        number2--;
        System.out.println(number2);

        int number3 = 3;
//        number3 = number3 + 5;
        number3 +=5;
        System.out.println(number3);

        int number4 = 3;
//        number4 = number4 - 5;
        number4 -=5;
        System.out.println(number4);

    }
}
