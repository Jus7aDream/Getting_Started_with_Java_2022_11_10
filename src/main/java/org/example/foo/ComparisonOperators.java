public class ComparisonOperators {
    public static void main(String[] args) {
        int khalidAge = 20;
        int mariamAge = 20;
        boolean isKhalidOlderThanMariam = khalidAge > mariamAge;
        System.out.println(isKhalidOlderThanMariam);
        System.out.println(khalidAge > mariamAge);
        System.out.println(khalidAge >= mariamAge);
        System.out.println(khalidAge < mariamAge);
        System.out.println(khalidAge <= mariamAge);
        System.out.println(khalidAge == mariamAge);
        System.out.println(khalidAge != mariamAge);
    }
}
